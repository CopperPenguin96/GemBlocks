        /// <param name="name">The name of the block</param>
        /// <param name="typetext">THe identifier of the block</param>
        public Block(uint type, uint meta, string name, string typetext)
        {
            Type = type;
            Meta = meta;
            Name = name;
            TypeText = typetext;
        }

